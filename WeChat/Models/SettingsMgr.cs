        public string updateClientSettings(string deviceId, string accountNo, string sessionID, string language
            , string encryptedPIN, string settingType, string settingValue)
        {
            HttpClientRequest hcr = new HttpClientRequest();
            hcr.headers.Clear();

            hcr.headers.Add(new KeyValuePair<string, string>("deviceId", deviceId));
            hcr.headers.Add(new KeyValuePair<string, string>("accountNo", accountNo));
            hcr.headers.Add(new KeyValuePair<string, string>("sessionId", sessionID));

            hcr.forms.Clear();
            hcr.forms.Add(new KeyValuePair<string, string>("settingType", settingType));
            hcr.forms.Add(new KeyValuePair<string, string>("settingValue", settingValue));

            string result = hcr.postRequest(Commons.SETTINGS_PMP_UPDATE_PATH, hcr.headers, hcr.forms, language);

            ILog logger = LogManager.GetLogger(MethodBase.GetCurrentMethod().DeclaringType);
            logger.Info(result + "");

            return result;
        }

